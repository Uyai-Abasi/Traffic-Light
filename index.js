alert("traffic light sign")
let input = prompt("follow the Signal")
var firstColor = "red"
var secondColor = "yellow"
var thirdColor = "green"
if (firstColor == input) {
    alert("STOP")
}
else if (secondColor == input) {
    alert("WAIT")
    alert("GET READY")
}
else if (thirdColor == input) {
    alert("GO")
}
else {
    alert("OOPS!")
}